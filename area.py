'''Module for holding functions to be used to calc aread of circlt, rectangle and triangle'''

def AreaOfCircle(r):
    '''function for area of circle'''
    pi = 3.14
    c_area = pi * r * r
    return c_area

def AreaOfRectangle (l, b):
    '''function for area of rectangle'''
    r_area = l * b
    return r_area

def AreaOfTriangele (b, h):
    '''function for area of triangle'''
    t_area = (b/2)*h
    return t_area